package src.utils;

import java.awt.Color;

/** 共通の色 */
public class CommonColor {

  /** グレー */
  public Color commonGray() {
    return new Color(0x9E9E9E);
  }

  /** 青 */
  public Color commonBlue() {
    return new Color(0x3f51b5);
  }

  /** 黒 */
  public Color commonBlack() {
    return new Color(0x000000);
  }

  /** 白 */
  public Color commonWhite() {
    return new Color(0xFFFFFF);
  }
}
