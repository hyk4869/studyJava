CREATE TABLE "T_TodoCategory" (
  id VARCHAR(64) NOT NULL PRIMARY KEY,
  createdById VARCHAR(64) NOT NULL REFERENCES "User"(id),
  createdByName VARCHAR(64) NOT NULL,
  updatedById VARCHAR(64) NOT NULL REFERENCES "User"(id),
  updatedByName VARCHAR(64) NOT NULL,
  createdAt TIMESTAMP WITH TIME ZONE NOT NULL,
  updatedAt TIMESTAMP WITH TIME ZONE NOT NULL,
  deletedAt TIMESTAMP WITH TIME ZONE NOT NULL,
  todoId VARCHAR(64) NOT NULL REFERENCES "T_TodoList"(id) ON DELETE CASCADE,
  categoryId VARCHAR(64) NOT NULL REFERENCES "T_Category"(id) ON DELETE CASCADE
);

